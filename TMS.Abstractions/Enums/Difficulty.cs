namespace TMS.Abstractions.Enums
{
    /// <summary>
    /// Specifies the difficulty level of a task.
    /// </summary>
    public enum Difficulty
    {
        /// <summary>
        /// Low difficulty. The task is easy to complete.
        /// </summary>
        Low = 0,

        /// <summary>
        /// Medium difficulty. The task requires a moderate amount of effort.
        /// </summary>
        Medium = 1,

        /// <summary>
        /// High difficulty. The task is challenging and requires significant effort.
        /// </summary>
        High = 2
    }
}